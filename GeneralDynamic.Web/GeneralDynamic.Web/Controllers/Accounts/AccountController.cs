        [Authorize(Roles = "Administrator")]
        [HttpGet]
        public async Task<IActionResult> GetAllServerUsers()
        {
            try
            {
                var serverUsers = await _userManager.Users.Where(x => x.IsActive).OrderByDescending(x => x.AddedOn).ToListAsync();


                var userList = serverUsers.Select(serverUser => new 
                {
                    Id = Guid.Parse(serverUser.Id),
                    UserName = serverUser.UserName,
                    Email = serverUser.Email,
                    FullName = serverUser.FullName,
                    AddedOn = serverUser.AddedOn.ToLocalTime().ToString(CultureInfo.CurrentCulture),
                    Phone = serverUser.PhoneNumber,

                    Role = _userManager.GetRolesAsync(serverUser).Result.FirstOrDefault()
                });



                return Ok(userList);
            }
            catch (Exception e)
            {
                Console.WriteLine(e);

            }

            return BadRequest();
        }

        [Authorize(Roles = "Administrator")]
        [HttpDelete("{id}")]

        public async Task<IActionResult> DeleteApplicationUser( string id)
        {
            try
            {
                var currentUserId = _userManager.GetUserId(HttpContext.User);
                if (currentUserId == id.ToString())
                {
                    return BadRequest($"You cannot delete yourself while you are logged in.");
                }

                var applicationUser = await _userManager.Users.FirstOrDefaultAsync(m => m.Id == id);
                if (applicationUser == null)
                {
                    return NotFound();
                }

                applicationUser.IsActive = false;
                await _userManager.UpdateAsync(applicationUser);




                return NoContent();
            }
            catch (Exception e)
            {
                Console.WriteLine(e.Message);
            }
            return BadRequest();
        }


        #region Helpers

        private void AddErrors(IdentityResult result)
        {
            foreach (var error in result.Errors)
            {
                ModelState.AddModelError(string.Empty, error.Description);
            }
        }

        private IActionResult RedirectToLocal(string returnUrl)
        {
            if (Url.IsLocalUrl(returnUrl))
            {
                return Redirect(returnUrl);
            }
            else
            {
                return RedirectToAction(nameof(PagesController.Dashboard), "Pages");
            }
        }

        #endregion
