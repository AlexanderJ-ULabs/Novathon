        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Register(RegisterViewModel model, string returnUrl = null)
        {

            ViewData["ReturnUrl"] = returnUrl;
            if (ModelState.IsValid)
            {
                var user = new ApplicationUser
                {
                    UserName = model.UserName,
                    FullName = model.FullName,
                    PhoneNumber = model.PhoneNumber,
                    Email = model.Email,
                    AddedOn = DateTime.UtcNow.ToUniversalTime(),
                    IsActive = true
                };

                if (model.UserPhoto?.Length > 0)
                {
                    using (var memoryStream = new MemoryStream())
                    {
                        await model.UserPhoto.CopyToAsync(memoryStream);
                        user.UserPhoto = memoryStream.ToArray();
                    }
                }


                if (!_userManager.Users.Any())
                {
                    if (!_roleManager.Roles.Any())
                    {
                        if (await _roleManager.FindByNameAsync("Administrator") == null ||
                            await _roleManager.FindByNameAsync("User") == null)
                        {
                            await _roleManager.CreateAsync(new IdentityRole() { Name = "Administrator" });
                            await _roleManager.CreateAsync(new IdentityRole() { Name = "User" });

                        }
                    }

                    var result = await _userManager.CreateAsync(user, model.Password);

                    if (result.Succeeded)
                    {
                        await _userManager.AddToRoleAsync(user, "Administrator");
                        _logger.LogInformation("User created a new account with password.");

                        var code = await _userManager.GenerateEmailConfirmationTokenAsync(user);
                        var callbackUrl = Url.EmailConfirmationLink(user.Id, code, Request.Scheme);
                        await _emailSender.SendEmailConfirmationAsync(model.Email, callbackUrl);

                        await _signInManager.SignInAsync(user, isPersistent: false);
                        _logger.LogInformation("Admin User created a new account with password.");
                        return RedirectToLocal(returnUrl);
                    }
                    AddErrors(result);
                }








            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }


